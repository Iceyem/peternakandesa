          <h3>Sejarah dan Asal Usul</h3>
          <p>
            "Makmur Agro Farm" didirikan dengan tujuan mulia untuk memenuhi kebutuhan umat Muslim dalam melaksanakan ibadah aqiqah dan qurban dengan cara yang mudah, nyaman, dan syar'i. Berawal dari kepedulian akan pentingnya ibadah aqiqah dan qurban dalam kehidupan seorang Muslim, kami mendirikan Makmur Agro dengan visi untuk menjadi solusi terbaik dalam penyediaan hewan aqiqah dan qurban yang berkualitas.
          </p>
    
          <h3>Awal Berdiri</h3>
          <p>
            Makmur Agro Farm didirikan pada tahun 2010 dengan komitmen untuk menyediakan hewan kurban berkualitas tinggi, termasuk kambing, domba, dan sapi. Berlokasi di Kampung Nagrog, Kecamatan Kemang, Bogor, kami telah berkembang pesat dalam bidang ini, berkat dedikasi dan kerja keras yang terus menerus.
            Kandang pertama kami fokus pada penyediaan hewan pedaging khusus untuk kurban, dengan penekanan pada kualitas dan kebersihan hewan yang kami jual. Setiap tahun, kami memastikan bahwa hewan kurban kami dipilih secara teliti dan dipersiapkan dengan baik untuk memenuhi tradisi keagamaan dengan penuh rasa hormat.
          </p>
    
          <h3>Visi dan Misi</h3>
          <p>
            Visi kami adalah menjadi penyedia layanan aqiqah dan qurban terpercaya yang mengedepankan kualitas, kehalalan, dan kemudahan. Untuk mencapai visi tersebut, kami menjalankan misi sebagai berikut:
            <ol>
              <li>Menyediakan hewan aqiqah dan qurban yang sehat dan sesuai dengan syariat Islam.</li>
              <li>Memberikan pelayanan terbaik kepada pelanggan dengan kemudahan proses pemesanan hingga pengantaran.</li>
              <li>Mengedukasi masyarakat tentang pentingnya ibadah aqiqah dan qurban serta tata cara pelaksanaannya yang benar.</li>
            </ol>
          </p>
    
          <h3>Perkembangan dan Inovasi</h3>
          <p>
            Di samping itu, kandang kedua kami merupakan pusat inovasi dalam pemuliaan genetika kambing. Kami menggunakan bibit unggul dan teknik perkawinan silang antara kambing lokal betina dengan pejantan Saanen untuk meningkatkan produksi susu kambing yang berkualitas. Kami berkomitmen untuk menjaga kesejahteraan hewan dan menghasilkan keturunan yang sehat dan produktif.
          </p>
    
          <h3>Komitmen Kami</h3>
          <p>
            Komitmen kami terhadap kualitas, kebersihan, dan kesejahteraan hewan menjadikan Makmur Agro Farm sebagai pilihan terbaik bagi mereka yang mencari hewan kurban dan susu kambing terbaik di Bogor dan sekitarnya.
          </p>
