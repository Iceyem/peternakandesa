  document.addEventListener('DOMContentLoaded', async function() {
  try {
    // Mengambil data dari API
    const response = await fetch('http://localhost/apipeternakandesa/readtestimoni.php');
    if (!response.ok) {
      throw new Error('Failed to fetch data');
    }
    const text = await response.text();
    const startIndex = text.indexOf('[');
    const cleanJson = text.substring(startIndex);
    const data = JSON.parse(cleanJson);

    // Mengambil elemen container testimonial
    const testimonialsContainer = document.querySelector('.testimonials');
    testimonialsContainer.innerHTML = ''; // Kosongkan container sebelum diisi

    // Mengisi testimonials dengan data dari API
    data.forEach(testimonial => {
      const testimonialItem = document.createElement('div');
      testimonialItem.classList.add('testimonial');
      testimonialItem.innerHTML = `
        <p>"${testimonial.isi}"</p>
        <div class="author">- ${testimonial.nama} -</div>
      `;
      testimonialsContainer.appendChild(testimonialItem);
    });

    // Set the first testimonial to be the 'center' testimonial
    let currentTestimonial = 1;
    function updateActiveTestimonial() {
      const testimonials = document.querySelectorAll('.testimonial');
      testimonials.forEach((testimonial, index) => {
        testimonial.classList.remove('left', 'center', 'right');
        testimonial.style.visibility = 'visible';

        if (index === currentTestimonial) {
          testimonial.classList.add('center');
        } else if (index === (currentTestimonial - 1 + testimonials.length) % testimonials.length) {
          testimonial.classList.add('left');
        } else if (index === (currentTestimonial + 1) % testimonials.length) {
          testimonial.classList.add('right');
        } else {
          testimonial.style.visibility = 'hidden';
          testimonial.style.position = 'absolute'; // Prevent layout shift
        }
      });
    }

    // Initial update to apply the correct classes
    updateActiveTestimonial();

    // Event listeners for navigation arrows
    document.querySelector('.left-arrow').addEventListener('click', () => {
      currentTestimonial = (currentTestimonial - 1 + data.length) % data.length;
      updateActiveTestimonial();
    });

    document.querySelector('.right-arrow').addEventListener('click', () => {
      currentTestimonial = (currentTestimonial + 1) % data.length;
      updateActiveTestimonial();
    });

  } catch (error) {
    console.error('Error fetching data:', error);
  }
});


